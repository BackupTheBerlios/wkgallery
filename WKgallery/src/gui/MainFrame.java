private void buttArtistaCodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttArtistaCodActionPerformed
    artistaCod = txtArtistaCod.getText();
    if(!artistaCod.isEmpty()){
    try {
        int i = Integer.valueOf(artistaCod).intValue();
        ar = artistaDAO.findArtista(i);
    } catch (RecordNonPresenteException ex) {
        Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
    }
    resetTable(modelArtisti);
    createRowArtisti(modelArtisti, ar);
    //ora mi occupo di popolare la tabella opere relativamente a questo artista
    Vector<Opera> op=operaDAO.selectOperaPerArtista(ar);
    resetTable(modelOpere);
    scanOpere(modelOpere,op);
    jTable2.setModel(modelOpere);
    txtPaneArtisti.setText("Ricerca effettuata per l'artista avente codice:" + artistaCod);
    txtPaneOpere.setText("Ricerca delle opere relative all'artista :" + ar.getCognome() + " " + ar.getNome());}
}//GEN-LAST:event_buttArtistaCodActionPerformed

