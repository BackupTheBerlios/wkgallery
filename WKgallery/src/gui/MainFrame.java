private void buttOperaCodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttOperaCodActionPerformed
    operaCod = txtOperaCod.getText();
    if (!operaCod.isEmpty()) {
        try {
            opera = operaDAO.findOpera(operaCod);
        } catch (RecordNonPresenteException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        resetTable(modelOpere);
        createRowOpere(modelOpere, opera);
        txtPaneOpere.setText("Ricerca effettuata per l'opera avente codice : " + operaCod + " Titolo : \"" + opera.getTitolo() + "\"");
        // ora svolgo la ricerca incrociata sui clienti cui l'opera è eventualmente legata tramite fattura
        if (opera.isVenduto()) {
            //cliFattOpera = opera.getFattura().getCliente();
            resetTable(modelCli);
            createRowClienti(modelCli, cliFattOpera);
            txtPaneClienti.setText("Cliente che ha acquistato l'opera: \"" + opera.getTitolo() + "\" dell'autore: " + opera.getArtista().getCognome() + " " + opera.getArtista().getNome());
        } else {
            txtPaneClienti.setText("Opera non ancora venduta, mostro tutti i clienti");
        }
    }
}//GEN-LAST:event_buttOperaCodActionPerformed

