private void buttClienteCogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttClienteCogActionPerformed
    String cliCog =txtCogCod.getText();
    cliPerCog.clear();
    if(!cliCog.isEmpty()){
    cliPerCog = clienteDAO.selectClientiPerStringa(cliCog);
    resetTable(modelCli);
    scanClienti(modelCli, cliPerCog);
    txtPaneClienti.setText("Elenco clienti il cui cognome / ragione sociale inizia per : " + cliCog);
    //cerca le fatture da cod cliente
    resetTable(modelFatture);
    fattPerCliCog.clear();
    for (Cliente c : cliPerCog)
    fattPerCliCog.addAll(fatturaDAO.selectFatturaPerCliente(c));
    scanFatture(modelFatture,fattPerCliCog);
    txtPaneFattura.setText("Elenco fatture relative a clienti il cui cognome / ragione sociale inizia per : " + cliCog);
    //cerca le opere dall'iniziale 
    resetTable(modelOpere);
    operePerCliCog.clear();
    for (Fattura f : fattPerCliCog)
    operePerCliCog.addAll(f.getOpere());
    scanOpere(modelOpere, operePerCliCog);
    txtPaneOpere.setText("Elenco opere acquistate da clienti il cui cognome / rag sociale inizia per : " + cliCog);
 
    }
    
}//GEN-LAST:event_buttClienteCogActionPerformed

